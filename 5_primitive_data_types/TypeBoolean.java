/* Boolean is type returned by all relational
 operators

 can only have true of false values
 true and false do not convert into any
 numerical representation


 The Boolean class has the methods for converting
 a boolean to a String and a String to a boolean

 example program that demonstrates the boolean type
 */

 public class TypeBoolean {
  public static void main(String args[]) {
    //Dynamic initialization
    boolean x = false;

    System.out.println("b is " + x);
    x = true;
    System.out.println("b is " + x);
  }
 }